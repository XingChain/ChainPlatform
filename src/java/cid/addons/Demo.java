package cid.addons;

import cid.Account;
import cid.BlockchainProcessor;
import cid.Cid;
import cid.util.Convert;
import cid.util.Logger;

public final class Demo implements AddOn {

    @Override
    public void init() {
        Cid.getBlockchainProcessor().addListener(block -> Logger.logInfoMessage("Block " + block.getStringId()
                + " has been forged by account " + Convert.rsAccount(block.getGeneratorId()) + " having effective balance of "
                + Account.getAccount(block.getGeneratorId()).getEffectiveBalanceCID()),
                BlockchainProcessor.Event.BEFORE_BLOCK_APPLY);
    }

    @Override
    public void shutdown() {
        Logger.logInfoMessage("Goodbye!");
    }

}
